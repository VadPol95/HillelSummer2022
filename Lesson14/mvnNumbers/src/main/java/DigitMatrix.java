public class DigitMatrix {
    public static String[][] getDigitsMatrix(int index) {
        return switch (index) {
            case 0 -> new String[][]{
                    {" @@@ "},
                    {"@   @"},
                    {"@   @"},
                    {"@   @"},
                    {"@   @"},
                    {" @@@ "}};
            case 1 -> new String[][]{
                    {"   @ "},
                    {"  @@ "},
                    {" @ @ "},
                    {"   @ "},
                    {"   @ "},
                    {"   @ "}};
            case 2 -> new String[][]{
                    {" @@@ "},
                    {"@   @"},
                    {"    @"},
                    {"  @@ "},
                    {" @   "},
                    {"@@@@@"}};
            case 3 -> new String[][]{
                    {"@@@@@"},
                    {"    @"},
                    {"  @@ "},
                    {"    @"},
                    {"@   @"},
                    {" @@@ "}};
            case 4 -> new String[][]{
                    {"  @ @"},
                    {" @  @"},
                    {"@   @"},
                    {"@@@@@"},
                    {"    @"},
                    {"    @"}};
            case 5 -> new String[][]{
                    {"@@@@@"},
                    {"@    "},
                    {"@@@@ "},
                    {"    @"},
                    {"    @"},
                    {"@@@@ "}};
            case 6 -> new String[][]{
                    {" @@@ "},
                    {"@    "},
                    {"@    "},
                    {"@@@@ "},
                    {"@   @"},
                    {" @@@ "}};
            case 7 -> new String[][]{
                    {"@@@@@"},
                    {"    @"},
                    {"   @ "},
                    {"  @  "},
                    {"  @  "},
                    {"  @  "}};
            case 8 -> new String[][]{
                    {" @@@ "},
                    {"@   @"},
                    {" @@@ "},
                    {"@   @"},
                    {"@   @"},
                    {" @@@ "}};
            case 9 -> new String[][]{
                    {" @@@ "},
                    {"@   @"},
                    {" @@@@"},
                    {"    @"},
                    {"    @"},
                    {" @@@ "}};
            default -> null;
        };
    }
}
